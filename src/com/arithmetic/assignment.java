package com.arithmetic;

public class assignment {
    public static void main(String[] args) {
        // Declaration
        int x;
        int y;

        // Fill the value
        x = 20;
        y = 25;

        // Addition
        y += x;

        // Print to the screen
        System.out.println("Addition : " + y);

        // Substraction
        y -= x;

        // FPrint to the screen
        System.out.println("Substraction : " + y);

        // Division
        y /= x;

        // Print to the screen
        System.out.println("Division : " + y);

        // Modulus
        y %= x;

        // Print to the screen
        System.out.println("Modulus : " + y);
    }
}
