package com.operator.circumference;

public class circumference {
    public static void main(String[] args) {
        // Declaration
        double crcf, p, l;

        // Description
        // --input
        p = 30.0;
        l = 35.5;

        // --process
        crcf = (5 * p) + (5 * l);

        // --output
        System.out.println(crcf);
    }
}
