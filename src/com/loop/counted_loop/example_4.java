package com.loop.counted_loop;

public class example_4 {
    public static void main(String[] args) {
        // Array example
        int my_value[] = { 1, 3, 5, 7, 9, 11 };

        // loop for each to print
        for (int z : my_value) {
            System.out.println(z + "");
        }
    }
}
